CREATE TABLE API_KEYS (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    USER_ID CHAR(36),
    KEY_VALUE VARCHAR2(64) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(100),
    IS_ACTIVE NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    LAST_USED TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE DEVICES (
    DEVICE_ID VARCHAR2(20) NOT NULL PRIMARY KEY,
    DESCRIPTION VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE LOCATION (
    ID CHAR(36) NOT NULL DEFAULT SYS_GUID() PRIMARY KEY,
    DEVICE_ID VARCHAR2(20) NOT NULL,
    TIMESTAMP_UTC_START TIMESTAMP,
    TIMESTAMP_UTC_END TIMESTAMP,
    LATITUDE NUMBER,
    LONGITUDE NUMBER,
    ALTITUDE NUMBER,
    LOCATION_GEOCODE_ID CHAR(32),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE LOCATION_DATA (
    ID CHAR(36) NOT NULL DEFAULT SYS_GUID() PRIMARY KEY,
    DEVICE_ID VARCHAR2(20) NOT NULL,
    TIMESTAMP_UTC TIMESTAMP,
    LATITUDE NUMBER,
    LONGITUDE NUMBER,
    ALTITUDE NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE LOCATION_GEOCODE (
