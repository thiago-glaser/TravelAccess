CREATE TABLE CARS (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    USER_ID CHAR(36),
    DESCRIPTION VARCHAR2(100),
    LICENSE_PLATE VARCHAR2(20),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0,
    CONSTRAINT FK_CARS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

ALTER TABLE SESSION_DATA ADD CONSTRAINT FK_SESSION_CAR FOREIGN KEY (CAR_ID) REFERENCES CARS(ID) ON DELETE SET NULL;

CREATE TABLE FUEL (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    USER_ID CHAR(36) NOT NULL,
    CAR_ID CHAR(36) NOT NULL,
    TIMESTAMP_UTC TIMESTAMP NOT NULL,
    TOTAL_VALUE NUMBER NOT NULL,
    LITERS NUMBER NOT NULL,
    TOTAL_KILOMETERS NUMBER DEFAULT 0,
    KILOMETER_PER_LITER NUMBER DEFAULT 0,
    PRICE_PER_KILOMETER NUMBER DEFAULT 0,
    RECEIPT_IMAGE BLOB,
    RECEIPT_MIME VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0,
    CONSTRAINT FK_FUEL_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_FUEL_CAR FOREIGN KEY (CAR_ID) REFERENCES CARS(ID) ON DELETE CASCADE
);

CREATE TABLE BLUETOOTH (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    USER_ID CHAR(36) NOT NULL,
    NAME VARCHAR2(100),
    DESCRIPTION VARCHAR2(255),
    ADDRESS VARCHAR2(100),
    CAR_ID CHAR(36),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0,
    CONSTRAINT FK_BLUETOOTH_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_BLUETOOTH_CAR FOREIGN KEY (CAR_ID) REFERENCES CARS(ID) ON DELETE SET NULL
);
