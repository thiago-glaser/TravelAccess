CREATE TABLE LOG (
    ID CHAR(36) NOT NULL DEFAULT SYS_GUID() PRIMARY KEY,
    DATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MESSAGE VARCHAR2(2000)
);

CREATE TABLE PARAMETER (
    KEY VARCHAR2(30) NOT NULL PRIMARY KEY,
    VALUE VARCHAR2(512),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE SCHEDULER_LOG (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    JOB_NAME VARCHAR2(100),
    RUN_DATE DATE,
    ROWS_UPDATED NUMBER,
    NOTES VARCHAR2(4000)
);

CREATE TABLE SESSION_DATA (
    ID CHAR(36) NOT NULL DEFAULT SYS_GUID() PRIMARY KEY,
    DEVICE_ID VARCHAR2(20) NOT NULL,
    CAR_ID CHAR(36),
    START_UTC TIMESTAMP,
    END_UTC TIMESTAMP,
    SESSION_TYPE CHAR(1),
    GEOCODE_START CHAR(32),
    GEOCODE_END CHAR(32),
    COST NUMBER,
    DISTANCE NUMBER,
    TIME_TRAVELED NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0
);

CREATE TABLE USERS (
    ID CHAR(36) DEFAULT SYS_GUID() PRIMARY KEY,
    USERNAME VARCHAR2(50) NOT NULL UNIQUE,
    PASSWORD_HASH VARCHAR2(255),           -- NULL for Google-only accounts
    EMAIL VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_ADMIN NUMBER,
    GOOGLE_ID VARCHAR2(255),               -- Google OAuth subject ID
    GOOGLE_AVATAR_URL VARCHAR2(500),       -- Google profile picture URL
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0,
    CONSTRAINT UQ_USERS_GOOGLE_ID UNIQUE (GOOGLE_ID)
);

CREATE TABLE USER_DEVICES (
    USER_ID CHAR(36) NOT NULL,
    DEVICE_ID VARCHAR2(50) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED NUMBER DEFAULT 0,
    CONSTRAINT PK_USER_DEVICES PRIMARY KEY (USER_ID, DEVICE_ID)
);


